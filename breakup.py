# Function to move files to their respective folders
def move_files(file_list, destination):
    for file_name in file_list:
        # Determine the source file path
        source_file = os.path.join(source_directory_path, file_name)
        # Determine the destination file path
        destination_file = os.path.join(destination, file_name)
        # Move the file
        shutil.move(source_file, destination_file)

# Iterate through the files in the source directory
for file_name in os.listdir(source_directory_path):
    # Check if the file is an image or a text file based on your file naming convention
    if file_name.endswith('.PNG') or file_name.endswith('.txt'):
        # Extract the base name without extension to check if it's in the validation list
        base_name = os.path.splitext(file_name)[0]
        if base_name in validation_images:
            # If the file is part of the validation set, move it to the validation directory
            move_files([file_name], validation_directory)
        else:
            # Otherwise, move it to the training directory
            move_files([file_name], training_directory)
